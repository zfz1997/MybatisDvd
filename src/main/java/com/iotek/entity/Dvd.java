package com.iotek.entity;

public class Dvd {
	    int id;
	    String dname;
	    int dcount;
	    int type;
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getDname() {
			return dname;
		}
		public void setDname(String dname) {
			this.dname = dname;
		}
		public int getDcount() {
			return dcount;
		}
		public void setDcount(int dcount) {
			this.dcount = dcount;
		}
		public int getType() {
			return type;
		}
		public void setType(int type) {
			this.type = type;
		}

}
